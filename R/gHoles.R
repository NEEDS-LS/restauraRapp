######    Fragments holes - Function to identify fragments holes
###########################################################################################
###########################################################################################

gHoles <- function(X, Y) {
  lss <-  gDifference(X, gUnionCascaded(Y), byid = TRUE)
  SpatialPolygons(c(if(is.null(lss)) NULL else lss@polygons))
}

